import java.util.*;
class Autoboxing
{
	public static void main(String args[])
	{
		int x=5;
		float y=3.14F;
		long z=600000;
	
	//Autoboxing
	Integer intobj=x;
	Float floatobj=y;
	Long longobj=z;
	
	System.out.println(intobj);
	System.out.println(floatobj);
	System.out.println(longobj);
	
	}
}